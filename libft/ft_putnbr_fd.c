#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	num;
	char			write_n;

	if (n < 0)
	{
		write(fd, "-", 1);
		num = (unsigned int)(n * -1);
	}
	else
		num = (unsigned int)n;
	if (num >= 10)
		ft_putnbr_fd(num / 10, fd);
	write_n = (num % 10) + '0';
	write(fd, &write_n, 1);
}
