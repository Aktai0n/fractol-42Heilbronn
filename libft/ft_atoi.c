#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	index;
	int	num;

	i = 0;
	index = 1;
	num = 0;
	while (str[i] == '\f' || str[i] == '\v' || str[i] == '\t'
		|| str[i] == '\n' || str[i] == ' ' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			index = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (index * num);
}
